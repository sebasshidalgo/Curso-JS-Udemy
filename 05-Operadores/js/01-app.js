
// Operador Mayor Que y Menor Que

const num1 = 20;
const num2 = "20";
const num3 = 30;

// Operador Mayor Que >
console.log(num1 > num3);
console.log(num3 > num1);
console.log(num1 > num2); // tira false porque num2 es un string

// Operador Menor Que <
console.log(num1 < num3);
console.log(num3 < num2); // tira false porque num2 es un string