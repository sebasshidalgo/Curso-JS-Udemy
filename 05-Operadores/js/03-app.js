
// Null y Undefined

// Undefined
let numero;

console.log(numero); // undefined
console.log(typeof numero); // undefined


// Null
let numero2 = null;
console.log(numero2); // null
console.log(typeof numero2); // object --> por estandar ECMA6 los datos null tienen que ser de tipo objeto


// Comparando misma sentencia con == y ===
console.log(numero == numero2); // comparando valor da true
console.log(numero === numero2); // comparando valor y tipo de dato da false --> siempre se recomienda comparar con ===




