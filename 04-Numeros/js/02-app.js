
// Operaciones con números

const num1 = 30;
const num2 = 20;

let resultado;

// Suma
resultado = num1 + num2;
console.log(resultado);

// Resta
resultado = num1 - num2;
console.log(resultado);

// Multiplicación
resultado = num1 * num2;
console.log(resultado);

// División
resultado = num1 / num2;
console.log(resultado);

// Módulo
resultado = num1 % num2;
console.log(resultado);