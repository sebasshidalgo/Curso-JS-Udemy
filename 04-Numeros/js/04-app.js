
// Incremento y Decremento

// de 1 en 1
let puntaje = 5;
console.log(puntaje);

puntaje++; 
console.log(puntaje);

++puntaje; 
console.log(puntaje);

puntaje--; 
console.log(puntaje);

--puntaje; 
console.log(puntaje);

// para incrementar/decrementar con valores mayor a 1
let puntaje2 = 10;
console.log(puntaje2);

puntaje2 += 3;
console.log(puntaje2);

puntaje2 += 5;
console.log(puntaje2);

puntaje2 -= 2;
console.log(puntaje2);

puntaje2 -= 1;
console.log(puntaje2);