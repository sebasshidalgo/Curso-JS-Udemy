
// Sintaxis vieja para crear variables ---> var

// Inicializar una variable con un valor
var producto = "Monitor de 24 Pulgadas";
console.log(producto);

// Las variables se pueden reasignar
producto = 'Monitor de 19 Pulgadas'; // se pueden utilizar comillas dobles " " o simples ' '
console.log(producto);

// JS es un lenguaje de tipo dinamico, no se especifica el tipo de dato
producto = 20;
console.log(producto);


// Se pueden inicializar sin valor y asignarlo despues
var disponible;
disponible = true;

