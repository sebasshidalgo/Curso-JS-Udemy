
// Arrow Function / Funciones Flecha ()=>

// Formato de Function Expression:
const aprendiendo = function() {
    console.log('Aprendiendo JavaScript1');
};
aprendiendo();

// Formato de Arrow Function ()=>
const aprendiendo2 = () => {
    console.log('Aprendiendo JavaScript2');
}
aprendiendo2();

// Si la arrow function tiene 1 sola linea puede omitirse las {} y el retorno es implicito
const aprendiendo3 = () => 'Aprendiendo JavaScript3';
console.log(aprendiendo3());

